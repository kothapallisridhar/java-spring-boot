package com.srk;

public class BitwiseOperators {
	
	public static void main(String[] args) {
		
		int a = 6;
		
		System.out.println(~a);
		
		// Unary bitwise compliment ~
		// << Signed left shift 
		// >> Signed right shift
        // >>> Unsigned right shift
		// & bitwise AND
		// | bitwise OR
		// ^ bitwise exclusive OR (XOR)
	}

}
